    /**
     * Delete annotation through the IO pipeline.
     *
     * Routes through `annotationResource.delete` so external guards (registered
     * via `IO_PIPELINE.registerGuard({ resource: 'annotation', direction: 'pre-delete', … })`)
     * may abort synchronously, and so any bound `crud:annotation` sink
     * receives the delete asynchronously in the background. Auto-history is
     * pushed via `inverseApply`. `rollbackOnAsyncRefuse: true` opts in to
     * server-refusal rollback (server reject reverts the local removal +
     * pops the history entry).
     *
     * @param {fabric.Object} annotation
     * @param _raise @private
     * @return {boolean} true if annotation was deleted (sync local outcome)
     */
    deleteAnnotation(annotation, _raise=true) {
        if (!annotation) return false;

        // If a doppelganger stand-in is on canvas in place of `annotation`
        // (FFT working copy, edit-mode preview, …), dismiss it first so the
        // canvas transitions stand-in → empty without flashing the original.
        const dop = annotation.internalID !== undefined
            ? this._trackedDoppelGangers[annotation.internalID]
            : undefined;
        if (dop) this._dismissDoppelganger(dop, annotation);

        // Clone so the inverse restores a structurally independent snapshot
        // — see deleteObject for the rationale.
        const f = annotation._factory?.();
        const restoreClone = f && typeof f.copy === 'function' ? f.copy(annotation) : annotation;
        // Preserve entity identity so subsequent undo/redo of *other* history
        // entries that captured this annotation by reference can still find
        // it on the canvas via incrementId/internalID after this entry's
        // inverse re-adds the clone.
        if (restoreClone !== annotation) {
            if (annotation.internalID !== undefined) restoreClone.internalID = annotation.internalID;
            if (annotation.incrementId !== undefined) restoreClone.incrementId = annotation.incrementId;
            if (annotation.sessionID !== undefined) restoreClone.sessionID = annotation.sessionID;
            if (annotation.id !== undefined) restoreClone.id = annotation.id;
        }
        const result = this.module.annotationResource.delete(annotation.incrementId, {
            apply:        () => { this._deleteAnnotation(annotation, _raise); },
            inverseApply: () => { this._addAnnotation(restoreClone, _raise); },
            meta: { kind: 'delete', object: annotation, viewerId: this.viewer?.uniqueId },
            rollbackOnAsyncRefuse: true,
        });
        return !!result.ok;
    }

    /**
     * Get annotation description from a preset, overriden by own object meta if present
     * @param {fabric.Object} annotation annotation to describe
     * @param {string} desiredKey metadata key to read and return
     * @param {boolean} defaultIfUnknown if false, empty string is returned in case no property was found
     * @param {boolean} withCoordinates if true, includes coordinates in the default name
     * @return {string|*} annotation description
     */
    getAnnotationDescription(annotation, desiredKey="category", defaultIfUnknown=true, withCoordinates=true) {
        let preset = this.module.presets.get(annotation.presetID);
        if (preset) {
            for (let key in preset.meta) {
                let objmeta = annotation.meta || {}, overridingValue = objmeta[key];
                let metaElement = preset.meta[key];
                if (key === desiredKey) {
                    return overridingValue || metaElement.value ||
                        (defaultIfUnknown ? this.getDefaultAnnotationName(annotation, withCoordinates) : "");
                }
            }
        }
        return defaultIfUnknown ? this.getDefaultAnnotationName(annotation, withCoordinates) : "";
    }

